func (fd *frontendFD) beforeSave() {
	// no-op
}

// afterLoad is invoked by stateify.
func (fd *frontendFD) afterLoad(ctx goContext.Context) {
	// no-op: nvproxy.afterLoad() => frontendFD.load() restores fd.hostFD
}

// beforeSave is invoked by stateify.
func (fd *uvmFD) beforeSave() {
	// no-op
}

// afterLoad is invoked by stateify.
func (fd *uvmFD) afterLoad(ctx goContext.Context) {
	fd.hostFD = openHostDevFileForRestore(ctx, "nvidia-uvm", fd.dev.nvp.useDevGofer, fd.containerName, fd.vfsfd.StatusFlags())
	if err := fdnotifier.AddFD(fd.hostFD, &fd.queue); err != nil {
		panic(fmt.Sprintf("fdnotifier.AddFD(%d) failed for UVM device: %v", fd.hostFD, err))
	}
	fd.memmapFile.SetFD(int(fd.hostFD))
}

// openHostDevFileForRestore differs from openHostDevFile in the following ways:
//   - The context passed to restore is not bound to a specific container, so
//     devutil.CtxDevGoferClient is not set. Instead, a container name is passed by the
//     caller.
//   - For the same reason, openHostDevFileForRestore does not return a container name.
//   - Restore is unable to propagate errors except via panics, so
//     openHostDevFileForRestore panics on failure rather than restoring an error.
func openHostDevFileForRestore(ctx goContext.Context, relpath string, useDevGofer bool, containerName string, openFlags uint32) int32 {
	if useDevGofer {
		clientProvider := devutil.GoferClientProviderFromContext(ctx)
		if clientProvider == nil {
			panic("devutil.CtxDevGoferClientProvider is not set")
		}
		devClient := clientProvider.GetDevGoferClient(containerName)
		hostFD, err := devClient.OpenAt(ctx.(context.Context), relpath, openFlags)
		if err != nil {
			panic(fmt.Sprintf("failed to open device gofer %s: %v", relpath, err))
		}
		return int32(hostFD)
	}
	abspath := filepath.Join("/dev", relpath)
	hostFD, err := unix.Openat(-1, abspath, int(openFlags&unix.O_ACCMODE|unix.O_NOFOLLOW), 0)
	if err != nil {
		panic(fmt.Sprintf("failed to open host %s: %v", abspath, err))
	}
	return int32(hostFD)
}

type restorableObjectImpl interface {
	objectImpl

	// Restore recreates the represented object in the given driver FD.
	Restore(ctx goContext.Context) error
}

// Restore implements restorableObjectImpl.Restore.
func (o *rmAllocObject) Restore(ctx goContext.Context) error {
	return o.params.restore()
}

// Restore implements restorableObjectImpl.Restore.
func (c *rootClient) Restore(ctx goContext.Context) error {
	// For root clients, the requested handle need to be passed as hRoot rather
	// than hObjectNew; see
	// src/nvidia/src/kernel/rmapi/entry_points.c:_nv04AllocWithAccessSecInfo()
	// => src/nvidia/src/kernel/rmapi/alloc_free.c:rmapiAllocWithSecInfo()
	// (renamed to hClient and *phObject respectively) => _rmAlloc() =>
	// src/nvidia/src/libraries/resserv/src/rs_server.c:serverAllocResource()
	// => serverAllocClient() => _serverCreateEntryAndLockForNewClient().
	// RS_COMPATABILITY_MODE is defined to 1 in src/nvidia/Makefile.
	temp := c.params
	temp.ioctlParams.HRoot = c.handle
	return temp.restore()
}
