package main

import (
	"fmt"
	"path/filepath"
	"strings"
)

const testTmpl = `// Autogenerated test for hostlayout check.
package {{.Package}}_hostlayout_test

import (
	"reflect"
	"testing"

	"{{.ImportPath}}"
	"gvisor.dev/gvisor/tools/go_hostlayout/hostlayoutcheck"
)

{{range .Structs}}
func TestHostLayout_{{.}}(t *testing.T) {
	if err := hostlayoutcheck.Validate(reflect.TypeOf((*{{$.Package}}.{{.}})(nil)).Elem()); err != nil {
		t.Errorf("Struct {{.}} failed hostlayout check: %v", err)
	}
}
{{end}}
`

type templateData struct {
	Package    string
	ImportPath string
	Structs    []string
}

func newTemplateData(pkg string, importPath string, structs []string) (*templateData, error) {
	idx := strings.Index(importPath, "pkg/")
	if idx == -1 {
		return nil, fmt.Errorf("failed to find 'pkg/' in %q", importPath)
	}
	return &templateData{
		Package:    pkg,
		ImportPath: filepath.Join("gvisor.dev", "gvisor", filepath.Dir(importPath[idx:])),
		Structs:    structs,
	}, nil
}
